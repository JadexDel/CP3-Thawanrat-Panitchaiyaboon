# Input
num1 = int(input("Enter 1st number: "))
num2 = int(input("Enter 2nd number: "))

# Process
plus = num1 + num2
minus = num1 - num2
multiply = num1 * num2
divide = num1 / num2

# Output
print(num1, "+", num2, "=", plus)
print(num1, "-", num2, "=", minus)
print(num1, "*", num2, "=", multiply)
print(num1, "/", num2, "=", divide)
